# 이름을 담은 문자열 벡터 생성
# 4명의 이름을 원소로 하는 문자 벡터 생성
name <- c("kim", "lee", "park", 'choi')

# 나이를 담은 숫자형 벡터 생성
# 4명의 나이를 원소로 하는 숫자 벡터 생성
age <- c(20, 32, 17, 51)

# 성별을 담은 범주형(factor) 벡터 생성
# as.factor()는 벡터를 범주형 데이터로 변환
# 결과: 레벨(수준)이 "F", "M"인 factor
sex <- as.factor(c("M", "F", "F", "F"))

# 위에서 만든 세 개의 벡터를 열로 하는 데이터 프레임 생성
# 데이터 프레임은 R에서 표 형태의 데이터를 저장하는 기본 구조
# 결과: 4행 3열의 데이터 프레임
dat <- data.frame(name, age, sex)

# dat 데이터 프레임에서 age 열을 추출
# $ 연산자는 데이터 프레임의 특정 열에 접근하는 데 사용
# 결과: c(20, 32, 17, 51)
dat$age

# dat 데이터 프레임에서 name 열을 추출
# 결과: c("kim", "lee", "park", "choi")
dat$name

# dat 데이터 프레임에서 sex 열을 추출
# 결과: factor 형태의 c("M", "F", "F", "F") 
dat$sex