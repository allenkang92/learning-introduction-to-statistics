# 10명의 키(cm)를 벡터로 저장
# c() 함수는 여러 값을 묶어서 벡터를 생성하는 함수
height <- c(165, 151, 162, 160, 151, 152, 159, 163, 143, 161)

# 1부터 3까지의 연속된 정수를 생성하여 변수 d에 할당
# : 연산자는 시작값부터 끝값까지의 정수 시퀀스를 생성합니다
# 결과: d는 c(1, 2, 3)이 됨
d <- 1:3

# seq() 함수로 1부터 9까지 2씩 증가하는 수열을 생성
# 첫번째 인자: 시작값, 두번째 인자: 끝값, 세번째 인자: 증가값
# 결과: e는 c(1, 3, 5, 7, 9)가 됨
e <- seq(1, 9, 2)

# rep() 함수는 지정된 값을 반복하여 벡터를 생성
# 첫번째 인자: 반복할 값, 두번째 인자: 반복 횟수
# 결과: f는 c(10, 10, 10, 10, 10)이 됨
f <- rep(10, 5)

# 벡터 d와 f를 하나의 벡터로 결합
# 결과: g는 c(1, 2, 3, 10, 10, 10, 10, 10)이 됨
g <- c(d, f)

# 4부터 1까지의 내림차순 정수와 0부터 9까지 3씩 증가하는 수열을 결합
# 결과: h는 c(4, 3, 2, 1, 0, 3, 6, 9)가 됨
h <- c(4:1, seq(0, 9, 3))