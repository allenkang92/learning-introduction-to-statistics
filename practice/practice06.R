# 점수 데이터를 벡터로 생성
score <- c(93, 83, 91, 68, 75)

# 기본 히스토그램 생성
hist(score)

# 제목이 없는 히스토그램 생성
hist(score, main = "") # main = ""은 제목을 없애는 옵션

# 히스토그램 추가 옵션 설명:
# xlab = ""은 x축 라벨을 없애는 옵션 
# ylab = ""은 y축 라벨을 없애는 옵션
# breaks = 5는 구간을 5개로 나누는 옵션
# seq(0, 240, 20)은 구간의 시작값과 끝값, 구간의 크기를 나타내는 벡터

