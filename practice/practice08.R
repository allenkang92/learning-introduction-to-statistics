# 기본 통계 함수 설명
# x가 데이터 벡터일 때 사용할 수 있는 기본 통계 함수들:
# 평균 mean(x)        # 모든 값의 합을 개수로 나눈 값 (산술평균)
# 분산 var(x)         # 각 값이 평균으로부터 떨어진 거리의 제곱의 평균 (표본분산, n-1로 나눔)
# 표준편차 sd(x)      # 분산의 제곱근, 데이터의 퍼짐 정도를 원래 단위로 표현
# 중앙값 median(x)    # 데이터를 크기순으로 나열했을 때 가운데 위치한 값
# 다섯수치 요약 fivenum(x)  # 최소값, 1사분위수, 중앙값, 3사분위수, 최대값을 순서대로 출력
# 사분위수 범위 IQR(x)      # 3사분위수에서 1사분위수를 뺀 값 (데이터 중간 50%의 퍼짐 정도)
# 범위 range(x)       # 최소값과 최대값을 순서대로 출력

# 회원 데이터 벡터 생성
member <- c(100, 100, 130, 78, 99, 200)

# 평균 계산
# 모든 회원 값의 합을 회원 수로 나눈 값
mean(member)  # (100+100+130+78+99+200)/6 = 117.8333...

# 분산 계산
# 각 값이 평균에서 얼마나 떨어져 있는지 제곱한 값들의 평균
# 표본분산이므로 n-1(5)로 나눔
var(member)  # ((100-117.8333)² + (100-117.8333)² + ... + (200-117.8333)²)/5 = 1729.767

# 표준편차 계산
# 분산의 제곱근, 원래 데이터와 같은 단위로 퍼짐 정도를 표현
sd(member)  # √1729.767 = 41.59049

# 중앙값 계산
# 데이터를 크기순으로 정렬했을 때 가운데 위치한 값
# 짝수 개의 데이터인 경우 가운데 두 값의 평균
median(member)  # 정렬: 78, 99, 100, 100, 130, 200 → (100+100)/2 = 100

# 다섯수치 요약
# 최소값, 1사분위수, 중앙값, 3사분위수, 최대값을 순서대로 출력
fivenum(member)  # 78, 99, 100, 130, 200

# 사분위수 범위 계산
# 3사분위수에서 1사분위수를 뺀 값 (데이터 중간 50%의 퍼짐 정도)
IQR(member)  # 130 - 99 = 31

# 범위 계산
# 최소값과 최대값을 순서대로 출력
range(member)  # 78, 200