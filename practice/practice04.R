# 10을 5번 반복한 벡터를 생성하여 n에 할당
# 결과: n은 c(10, 10, 10, 10, 10)
n <- rep(10, 5)

# 1부터 5까지의 연속된 정수 벡터를 o에 할당
# 결과: o는 c(1, 2, 3, 4, 5)
o <- 1:5

# n과 o 벡터를 열(column)로 결합하여 행렬 p 생성
# cbind는 "column bind"의 약자
# 결과: p는 5행 2열 행렬 (각 열은 n과 o)
p <- cbind(n, o)

# n과 o 벡터를 행(row)으로 결합하여 행렬 q 생성
# rbind는 "row bind"의 약자
# 결과: q는 2행 5열 행렬 (각 행은 n과 o)
q <- rbind(n, o)

# 1부터 4까지의 정수로 2x2 행렬 r 생성
# 기본적으로 열 우선으로 값이 채워짐
# 결과: r은 [1 3; 2 4] 행렬
r <- matrix(1:4, 2, 2)

# 벡터 c(1, 4, 2, 7)로 2x2 행렬 s 생성
# 결과: s는 [1 2; 4 7] 행렬
s <- matrix(c(1, 4, 2, 7), 2, 2)

# 행렬 r과 s를 더함 (각 위치의 요소끼리 더함)
# 결과: [2 5; 6 11] 행렬
r + s

# 행렬 s의 역행렬을 계산
# solve() 함수는 선형방정식의 해를 구하거나 역행렬을 계산
# 결과: det(s) = 1*7 - 4*2 = -1이므로 [7 -2; -4 1]/(-1) = [-7 2; 4 -1]
solve(s)

# s 행렬의 1행 2열 요소 반환
# 결과: 2
s[1, 2]

# s 행렬의 1행 전체 반환
# 결과: c(1, 2)
s[1, ]

# s 행렬의 2열 전체 반환
# 결과: c(2, 7)
s[, 2]