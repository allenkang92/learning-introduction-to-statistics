# 나이 데이터를 벡터로 생성
age <- c(57, 58, 59, 60, 48, 47, 36, 10, 100, 55)

# 박스플롯(상자그림) 생성
# 박스플롯은 데이터의 분포를 시각적으로 보여주는 그래프로 다음 정보를 표시함:
# - 중앙값(median): 상자 내부의 굵은 선
# - 1사분위수(Q1): 상자의 아래쪽 경계
# - 3사분위수(Q3): 상자의 위쪽 경계
# - 최소값: 아래쪽 수염의 끝
# - 최대값: 위쪽 수염의 끝
# - 이상치(outlier): 수염 밖에 점으로 표시됨
boxplot(age, ylab = "Age")

# 박스플롯의 추가 옵션:
# main = "제목": 그래프 제목 설정
# xlab = "X축 라벨": x축 라벨 설정
# col = "색상": 박스 내부 색상 설정
# horizontal = TRUE: 가로 방향 박스플롯 생성
# notch = TRUE: 중앙값 신뢰구간을 표시하는 홈 추가

